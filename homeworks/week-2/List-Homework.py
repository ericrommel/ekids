a = [1, 25, 732, 84]
# To add to a list use {a.append()}. e.g
a.append(2)
print(a)

# To remove an item from a list use {a.pop(index)}. e.g
a.pop(2)
print(a)

# To retrieve an item from a list use {print(a[index])}. e.g
print(a[2])

# To change an item on a list use {a[index] = new number}. e.g
a[3] = 54
print(a)

# To calculate te lenght of the list use {len(a)}. e.g
print(len(a))
